#include "push_swap.h"

long	ft_atoi(const char *str)
{
	long	num;
	int		multiply_for_negative;
	int		i;

	num = 0;
	multiply_for_negative = 1;
	i = 0;
	while (str[i] && (str[i] == ' ' || str[i] == '\t'
			|| str[i] == '\n' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\f'))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		multiply_for_negative *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (num * multiply_for_negative);
}
